test("Placing ships adds ships correctly to ships array", () => {
    const game = new GameBoard;
    game.placeShip(3, [0, 0]);

    expect(game.ships).toEqual([{ length: 3,
                                   hits: 0, 
                                   sunk: false, 
                                   coordinates: [[0, 0], [0, 1], [0, 2]] }]);
});